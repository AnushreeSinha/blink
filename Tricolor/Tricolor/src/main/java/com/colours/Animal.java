
package com.colours;


public class Animal {

    private String Name;
    private Integer Size;
    private Long id;

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public Integer getSize() {
        return Size;
    }

    public void setSize(Integer Size) {
        this.Size = Size;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

}
